// Importa a classe "ExemploFor" para o pacote "com.fiap.view"
package com.fiap.view;

// Declaração da classe "ExemploFor"
public class For {
    
    // Método principal (ponto de entrada) do programa
    public static void main(String[] args) {
        
        // Inicia um loop "for" que irá contar de 0 a 10 (inclusive)
        for (int i = 0; i <= 10; i++) {
            
            // Imprime o valor atual de "i" na tela
            System.out.println(i);
        }
        
        // Fim do método principal
    }
    
    // Fim da classe "ExemploFor"
}
